    private void serchPatient() {
        try {
            PatientDTO patientDTO=PatientController.searchPatientDTO(txtPatientID.getText());
            lblNic1.setText(patientDTO.getNic());
            lblName.setText(patientDTO.getFirstName()+" "+patientDTO.getMiddleName()+" "+patientDTO.getLastName());
            lblBlood.setText(patientDTO.getBloodGrup());
            lbldob.setText(patientDTO.getDob());
            lbltel.setText(patientDTO.getTel()+"");
            txtAddress.setText(patientDTO.getAddress());
            lblCons.setText(patientDTO.getConsultantDTO().getEmployeeDTO().getFirstName()+" "+patientDTO.getConsultantDTO().getEmployeeDTO().getMiddleName()+" "+patientDTO.getConsultantDTO().getEmployeeDTO().getLastName());
            
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(TestDetailForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

    private void loadTestCm() {
        try {
            ArrayList<TestDTO>arrayList=TestController.getTestDTOs();
            for (TestDTO testDTO : arrayList) {
                cmbName.addItem(testDTO.getDescription());
                cmbNu.addItem(testDTO.getTid());
            }
        } catch (SQLException ex) {
            Logger.getLogger(TestDetailForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TestDetailForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void set() {
        try {
            String id=(String) cmbNu.getSelectedItem();
            TestDTO  dTO=TestController.searchTest(id);
            txtPrice.setText(dTO.getPrice()+"");
        } catch (SQLException ex) {
            Logger.getLogger(TestDetailForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TestDetailForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
