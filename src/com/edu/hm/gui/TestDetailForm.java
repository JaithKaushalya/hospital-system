    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Date date=new Date();
            
            String tdid=date.getDate()+txtPatientID.getText();
            TestDTO dTO=TestController.searchTest(cmbNu.getSelectedItem().toString());
            PatientDTO patientDTO=PatientController.searchPatientDTO(txtPatientID.getText());
            String result=jTextArea1.getText();
            
            TestDetailDTO detailDTO=new TestDetailDTO(tdid, dTO, patientDTO, result, Double.parseDouble(txtPrice.getText()));
            boolean add=TestDetailController.addTest(detailDTO);
            if(add){
                JOptionPane.showMessageDialog(this,"test result added ");
            }
        } catch (SQLException ex) {
            Logger.getLogger(TestDetailForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TestDetailForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

